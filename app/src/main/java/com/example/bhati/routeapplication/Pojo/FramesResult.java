package com.example.bhati.routeapplication.Pojo;

public class FramesResult {

    String street;
    String vegetation;
    String car;
    String snpashot;
    String person;


    public FramesResult(){

    }

    public String getPerson() {
        return person;
    }

    public void setPerson(String person) {
        this.person = person;
    }

    public String getStreet() {
        return street;
    }

    public void setStreet(String street) {
        this.street = street;
    }

    public String getVegetation() {
        return vegetation;
    }

    public void setVegetation(String vegetation) {
        this.vegetation = vegetation;
    }

    public String getCar() {
        return car;
    }

    public void setCar(String car) {
        this.car = car;
    }

    public String getSnpashot() {
        return snpashot;
    }

    public void setSnpashot(String snpashot) {
        this.snpashot = snpashot;
    }
}
